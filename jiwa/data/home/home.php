<h2>Definisi Gangguan Jiwa</h2>
<hr>
<p>Gangguan jiwa adalah pola perilaku atau psikologik yang secara klinis bermakna dan secara khas berkaitan dengan gejala, penderitaan (distress) serta hendaya (impairment) dalam fungsi psikososial. Gangguan jiwa adalah gangguan dalam : cara berpikir (cognitive), kemauan (volition,emosi (affective), tindakan (psychomotor). Dari berbagai penelitian dapat dikatakan bahwa gangguan jiwa adalah kumpulan dari keadaan-keadaan yang tidak normal, baik yang berhubungan dengan fisik, maupun dengan mental. Keabnormalan tersebut dibagi ke dalam dua golongan yaitu : gangguan jiwa (Neurosa) dan Sakit jiwa (psikosa). Keabnormalan terlihat dalam berbagai macam gejala yang terpenting diantaranya adalah: ketegangan(tension), rasa putus asa dan murung, gelisah, cemas, perbuatan-perbuatan yang terpaksa(Convulsive), hysteria, rasa lemah, tidak mampu mencapai tujuan, takut, pikiran-pikiranburuk dsb.<td></td>
</p> 
